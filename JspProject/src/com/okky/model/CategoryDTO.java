package com.okky.model;

public class CategoryDTO {
	private int cate_num;
	private String cate_name;
	private int cate_group;
	private int cate_step;

	public int getCate_num() {
		return cate_num;
	}

	public void setCate_num(int cate_num) {
		this.cate_num = cate_num;
	}

	public String getCate_name() {
		return cate_name;
	}

	public void setCate_name(String cate_name) {
		this.cate_name = cate_name;
	}

	public int getCate_group() {
		return cate_group;
	}

	public void setCate_group(int cate_group) {
		this.cate_group = cate_group;
	}

	public int getCate_step() {
		return cate_step;
	}

	public void setCate_step(int cate_step) {
		this.cate_step = cate_step;
	}

}
