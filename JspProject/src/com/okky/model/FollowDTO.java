package com.okky.model;

public class FollowDTO {
	private int follow_num;
	private int follow_mem;
	private int follow_target;

	public int getFollow_num() {
		return follow_num;
	}

	public void setFollow_num(int follow_num) {
		this.follow_num = follow_num;
	}

	public int getFollow_mem() {
		return follow_mem;
	}

	public void setFollow_mem(int follow_mem) {
		this.follow_mem = follow_mem;
	}

	public int getFollow_target() {
		return follow_target;
	}

	public void setFollow_target(int follow_target) {
		this.follow_target = follow_target;
	}

}
